package pl.kurs.zad;

public class Calculator {
    double addTwoNumbers(double a1, double a2){
        return a1 + a2;

    }
    double addThreeNumbers(double a1, double a2, double a3){
        return a1 + a2 + a3;
    }
    double subtractTwoNumbers(double a1, double a2){
        return  a1 - a2;
    }
    double multiplyTwoNumbers(double a1, double a2){
        return a1 * a2;
    }
    double divisionTwoNumbers( double a1, double a2){
        return a1 / a2;
    }
}
